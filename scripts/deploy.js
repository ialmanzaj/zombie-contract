const main = async () => {
    const [deployer] = await hre.ethers.getSigners();
    const accountBalance = await deployer.getBalance();
  
    console.log("Deploying contracts with account: ", deployer.address);
    console.log("Account balance: ", accountBalance.toString());
  
    const zombieContractFactory = await hre.ethers.getContractFactory("ZombieFactory");
    const zombieFactory = await zombieContractFactory.deploy();
    await zombieFactory.deployed();
  
    console.log("zombieFactory address: ", zombieFactory.address);
  };
  
  const runMain = async () => {
    try {
      await main();
      process.exit(0);
    } catch (error) {
      console.log(error);
      process.exit(1);
    }
  };
  
  runMain();